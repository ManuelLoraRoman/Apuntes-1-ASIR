
#Calcular el perímetro y área de un rectángulo dada su base y su altura.

base = int(input("Dime la base del rectángulo: "))
altura = int(input("Dime ahora la altura del rectángulo: "))
print("El perimetro del rectángulo sería %d y el área sería %d."%((base * 2) + (altura * 2),base * altura))
