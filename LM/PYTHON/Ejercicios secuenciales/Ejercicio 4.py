
#Dados dos números, mostrar la suma, resta, división y multiplicación de ambos.

num1 = int(input("Dime un número:"))
num2 = int(input("Dime otro número:"))

print("Nº1 : ", num1)
print("Nº2 : ", num2)

print("La suma de los dos número es %d"%(num1 + num2))
print("La resta de los dos número es %d"%(num1 - num2))
print("La división de los dos número es %d"%(num1 // num2))
print("La multiplicación de los dos número es %d"%(num1 * num2))
